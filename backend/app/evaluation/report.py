"""
SYNAPSE Evaluation Report Generator
----------------------------------

Generates professional evaluation reports for the
SYNAPSE AIOps Platform.

Outputs:
    • Console Report
    • JSON Report
    • Markdown Report

"""

from __future__ import annotations

from typing import Dict, Any

from app.evaluation.utils import (
    save_json_report,
    save_markdown_report,
    current_timestamp,
)


class EvaluationReport:

    def __init__(self, benchmark_result: Dict[str, Any]):

        self.data = benchmark_result

    # -------------------------------------------------

    @staticmethod
    def calculate_grade(score: float) -> str:

        if score >= 95:
            return "A+"

        if score >= 90:
            return "A"

        if score >= 80:
            return "B+"

        if score >= 70:
            return "B"

        if score >= 60:
            return "C"

        return "Needs Improvement"

    # -------------------------------------------------

    def build_console_report(self):

        metrics = self.data.get("metrics", {})

        overall = metrics.get("overall", 0)

        grade = self.calculate_grade(overall)

        print("\n")
        print("=" * 70)
        print("              SYNAPSE AI EVALUATION REPORT")
        print("=" * 70)

        print(f"Generated : {current_timestamp()}")

        print("\nPerformance Metrics")

        print("-" * 70)

        print(f"Accuracy      : {metrics.get('accuracy',0):.3f}")
        print(f"Precision@K   : {metrics.get('precision',0):.3f}")
        print(f"Recall@K      : {metrics.get('recall',0):.3f}")
        print(f"F1 Score      : {metrics.get('f1',0):.3f}")

        print()

        rag = self.data.get("rag", {})

        print("RAG")

        print("-" * 70)

        print(f"Status        : {rag.get('status')}")
        print(f"Latency       : {rag.get('latency',0)}")
        print(f"Documents     : {rag.get('documents',0)}")

        print()

        orchestrator = self.data.get("orchestrator", {})

        print("Orchestrator")

        print("-" * 70)

        print(f"Status        : {orchestrator.get('status')}")

        print()

        print(f"Overall Score : {overall}")

        print(f"Grade         : {grade}")

        print("=" * 70)

    # -------------------------------------------------

    def export_json(self, filename="evaluation_reports/report.json"):

        save_json_report(

            self.data,

            filename

        )

    # -------------------------------------------------

    def export_markdown(self, filename="evaluation_reports/report.md"):

        metrics = self.data.get("metrics", {})

        overall = metrics.get("overall", 0)

        grade = self.calculate_grade(overall)

        markdown = f"""
# SYNAPSE Evaluation Report

Generated: {current_timestamp()}

---

## Performance

| Metric | Value |
|--------|-------|
| Accuracy | {metrics.get("accuracy")} |
| Precision@K | {metrics.get("precision")} |
| Recall@K | {metrics.get("recall")} |
| F1 Score | {metrics.get("f1")} |
| Overall Score | {overall} |
| Grade | {grade} |

---

## RAG

Status: **{self.data.get("rag",{}).get("status")}**

Documents Indexed:
{self.data.get("rag",{}).get("documents",0)}

Latency:
{self.data.get("rag",{}).get("latency",0)}

---

## Orchestrator

Status:
{self.data.get("orchestrator",{}).get("status")}

---

Generated by SYNAPSE Evaluation Framework
"""

        save_markdown_report(

            markdown,

            filename

        )

    # -------------------------------------------------

    def export_all(self):

        self.export_json()

        self.export_markdown()

        self.build_console_report()


# ------------------------------------------------------

if __name__ == "__main__":

    from app.evaluation.benchmark import SynapseBenchmark

    benchmark = SynapseBenchmark()

    results = benchmark.run()

    report = EvaluationReport(results)

    report.export_all()

    print("\nReports Generated Successfully.\n")